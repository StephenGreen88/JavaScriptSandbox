// Set a const variable
const x = 20;

if (x === 10) {
    console.log('x is 10');
} else if (x > 10) {
    console.log('x is greater than 10');
} else {
    console.log('x is less than 10');
};

// Multiple conditions
const y = 10
if (x > 5 || y > 10) {
    console.log('y is 10');
} else if (y > 10) {
    console.log('y is greater than 10');
} else {
    console.log('y is less than 10');
};